import math
a = 6.25
b = 1.44
print('a = ', a)
print('b = ', b)

# membulatkan ke atas
print("Hasil pebulatan ke atas dari a adalah ", math.ceil(a))
print("Hasil pebulatan ke atas dari b adalah ", math.ceil(b))

# membulatkan ke bawah
print("Hasil pebulatan ke bawah dari a adalah ", math.floor(a))
print("Hasil pebulatan ke bawah dari b adalah ", math.floor(b))

# melakukan pengakaran
print("Hasil pengakran dari a adalah ", math.sqrt(a))
print("Hasil pengakran dari b adalah ", math.sqrt(b))

c = [2, 4, 3, 5, 28, 7, 22 ,15]
# mencari nilai max dan min
print("Nilai max pada list c adalah ", max(c))
print("Nilai min pada list c adalah ", min(c))